'''Demonstração do funcionamento dos operadores relacionais'''
a = 13
b = 5

print("Operadores Relacionais (==, !=, <, >, <=, >=)")
print("Considerando a =", a, "e b =", b)
print("a == b = ", a == b)
print("a != b = ", a != b)
print("a < b = ", a < b)
print("a > b = ", a > b)
print("a <= b = ", a <= b)
print("a >= b = ", a >= b)
