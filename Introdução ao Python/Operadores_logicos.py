
"""Demonstração do funcionamento dos operadores lógicos"""
x=13
y=5
z=2
print("Operadores Lógicos (and, or, not)")
print ("Considerando x=",x," e y=",y,"z =",z)
#Operador And (Conjunção)
print("(x>y) and (y<z) =", (x>y) and (y<z))
#Operador Or (Disjunção)
print("(x>y) or (y<z) =", (x>y) or (y<z))
#Operador Not (Negação)
print("not (x>y) =",not (x>y))